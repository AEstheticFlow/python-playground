# ---------- exampleModule.py ---------- #
# Example module containing constants and functions

# Constant
pi = 3.14159

# Functions
def square(x):
    """Return the square of x."""
    return x ** 2

def cube(x):
    """Return the cube of x."""
    return x ** 3

def circumference(radius):
    """Return the circumference of a circle with given radius."""
    return 2 * pi * radius

def area(radius):
    """Return the area of a circle with given radius."""
    return pi * radius ** 2
